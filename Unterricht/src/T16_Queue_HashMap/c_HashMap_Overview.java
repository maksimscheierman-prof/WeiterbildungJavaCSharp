package T16_Queue_HashMap;

public class c_HashMap_Overview {
    /**                                     INFO
     *                                     HashMap
     * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * *
     *
     *          Eine HashMap ist eine Datenstruktur die "Schlüssel"-"Wert" Paare speichert.
     *          Sie ähnelt einer ArrayList, kann jedoch zwei zugehörige Werte gleichzeitig speichern.
     *
     *          Merkmale:
     *          1. Schlüssel-Wert Paare : Jeder Schlüssel ist eindeutig und wird verwendet,
     *             um auf den zugehörigen Wert zuzugreifen
     *          2. Keine gehordnete Reihenfolge : Die Elemente sind nicht in einer bestimmten Reihenfolge
     *                                              gespeichert. Die Reihenfolge kann sich sogar ändern.
     *          3. In einer HashMap kann es nur einen "null"-Schlüssel geben, aber beliebig viele null-Werte
     *
     *          Wichtige Methoden:
     *          - put(key, value) Fügt ein Schlüssel-Wert Paar in die HashMap ein. Bereits vorhandene Schlüssel
     *                            werden überschrieben.
     *          - get(key) : Gibt den Wert zurück der dem Schlüssel zugeordnet ist (value)
     *                       gibt null zurück wenn es den Schlüssel nicht gibt.
     *          - remove(key) : Entfernt das Schlüssel-Wert Paar
     *          - containsKey(key) : Prüft ob der key vorhanden ist.
     *          - containsValue(value) : Prüft ob der Wert vorhanden ist.
     *          - size() : gibt die Anzahl der Paare zurück.
     *
     *
     *          Hinweis zum Durchlaufen aller Elemente der Hashmap kann wie folgt vorgegangen werden:
     *              for (var key : meineHashMap.keySet()) {
     *                  System.out.println(key);
     *              }
     * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * *
     */
}
